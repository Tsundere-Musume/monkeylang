#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Illegal,
    EOF,
    // Identifiers + literal,
    Ident(String),
    Int(i64),
    // Delimiter,
    Comma,
    Semicolon,
    Lparen,
    Rparen,
    Lbrace,
    Rbrace,
    // Operator,
    Assign,
    Plus,
    Minus,
    Bang,
    Asterisk,
    Slash,
    Lt,
    Gt,
    Eq,
    NotEq,
    // Keyword,
    Function,
    Let,
    True,
    False,
    If,
    Else,
    Return,
}
